/**
 * 장르에 맞는 가이드라인을 반환합니다.
 * 매칭되는 장르가 없으면 빈 문자열을 반환합니다.
 */
export function getGenreGuideline(
  genre: string | undefined,
  step: "synopsis" | "characters" | "world" | "plot"
): string {
  if (!genre) return "";
  const guideline = GENRE_GUIDELINES[genre];
  if (!guideline) return "";
  return "\n\n" + guideline[step];
}
