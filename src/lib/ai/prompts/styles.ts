/**
 * 문체 스타일의 프롬프트를 가져옵니다.
 * 사용자가 커스텀한 프롬프트가 있으면 그것을 반환하고,
 * 없으면 기본 프롬프트를 반환합니다.
 */
export function getStylePrompt(
  styleName: string,
  customStylePrompts: Record<string, string>
): string {
  // 사용자 커스텀 프롬프트가 있으면 우선
  if (customStylePrompts[styleName]) {
    return customStylePrompts[styleName];
  }
  // 기본 프롬프트가 있으면 반환
  if (DEFAULT_STYLE_PROMPTS[styleName]) {
    return DEFAULT_STYLE_PROMPTS[styleName];
  }
  // 사용자가 추가한 커스텀 스타일인데 프롬프트가 없으면 기본 안내
  return `문체 스타일: ${styleName}`;
}
